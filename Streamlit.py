import streamlit as st
import pandas as pd
import joblib

# 1. Load your 'frozen' model
model = joblib.load('housing_model.pkl')

# 2. Create the User Interface (the website part)
st.title("California House Price Predictor 🏠")
income = st.slider("What is the median income of the area?", 0.0, 15.0, 3.5)
age = st.number_input("What is the house age?", 1, 50, 20)

# 3. Make the prediction on the fly
if st.button("Predict Price"):
    prediction = model.predict([[income, age]]) # Plus your other features
    st.success(f"The estimated house value is: ${prediction[0]:,.2f}")